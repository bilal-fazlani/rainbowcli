package com.bilalfazlani.scala.rainbow

class RainbowExtensionTest extends munit.FunSuite {
  test("Rainbow should colorize strings") {
    val red = "red".red
    val blue = "blue".blue
    val yellow = "yellow".yellow
    val green = "green".green

    println("red = " + red)
    println("blue = " + blue)
    println("yellow = " + yellow)
    println("green = " + green)
  }
}
